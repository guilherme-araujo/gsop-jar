package br.ufrn.imd.bioinfo.gsop.model;

public class Eph {
	
	private double bonus;
	private int time;
	
	public Eph(double bonus) {
		this.bonus = bonus;
	}

	public double getBonus() {
		return bonus;
	}

	public void setBonus(double bonus) {
		this.bonus = bonus;
	}

	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}
	
	

}
